    public InformacionEquipo() {
        initComponents();
        user = login.user;
        IDequipo = Informacion_Clientes.IDequipo;
        Idcliente_update = GestionarClientes.IDcliente_update;
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
            "select nombre_cliente from clientes where idclientes ='" + Idcliente_update + "'");
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                nom_cliente = rs.getString("nombre_cliente");
            }
        } catch (Exception e) {
            System.err.println("Error al consultar el nombre del cliente" + e);
        }
        
        try {
            Connection cn = Conexion.conectar();
            PreparedStatement pst = cn.prepareStatement(
            "select * from equipos where idequipos = '" + IDequipo + "'");
            ResultSet rs = pst.executeQuery();
            
            if(rs.next()){
                cmb_tipoequipo.setSelectedItem(rs.getString("tipo_equipo"));
                cmb_marcas.setSelectedItem(rs.getString("marca"));
                cmb_status.setSelectedItem(rs.getString("status"));
                txt_modelo.setText(rs.getString("modelo"));
                txt_numSerie.setText(rs.getString("num_serie"));
                txt_ultmodi.setText(rs.getString("ultima_modificacion"));
                
//                procedimiento para obtener las fechas
                String dia="", mes="", annio="";
                dia = rs.getString("dia_ingreso");
                mes = rs.getString("mes_ingreso");
                annio = rs.getString("annio_ingreso");
                txt_fecha.setText(dia + " del " + mes + " del " + annio);
                
                jTextPane_observaciones.setText(rs.getString("observaciones"));
                jTextPane_comentariosTecnicos.setText(rs.getString("comentarios_tecnicos"));
                jlabel_revtecnicade.setText("Comentarios y actualuizacion del tecnico: " + rs.getString("revision_tecnica"));
                
                
            }
            
        } catch (Exception e) {
            System.err.println("Erro al consultar la informacion del equipo");
        }
        setTitle("Equipo del cliente " + nom_cliente);
        setSize(670, 530);
        setResizable(false);
        setLocationRelativeTo(null);
        
        setDefaultCloseOperation(WindowConstants.DISPOSE_ON_CLOSE);
        ImageIcon fondo = new ImageIcon("src/images/wallpaperPrincipal.jpg");
        //creamos nuevo objeto para definir las dimesiones de la imagen
        //y se ajusten a nuestro JLabel
        Icon icono = new ImageIcon(fondo.getImage().getScaledInstance(jlabel_wallpapper.getWidth(),
                jlabel_wallpapper.getHeight(), Image.SCALE_DEFAULT));
        //colocar la imagen que estamos escalando dentro del jlabel
        jlabel_wallpapper.setIcon(icono);
        this.repaint();
        
        txt_nombreCliente.setText(nom_cliente);
    }
    
