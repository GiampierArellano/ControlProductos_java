    private void jButton_actualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton_actualizarActionPerformed
        // TODO add your handling code here:
        int validacion = 0;
        String tipo_equipo, marca, modelo, num_serie, status, observaciones;
        
        tipo_equipo = cmb_tipoequipo.getSelectedItem().toString();
        marca = cmb_marcas.getSelectedItem().toString();
        status = cmb_status.getSelectedItem().toString();
        modelo = txt_modelo.getText().trim();
        num_serie = txt_numSerie.getText().trim();
        observaciones = jTextPane_observaciones.getText();
        
        if(modelo.equals("")){
            txt_modelo.setBackground(Color.red);
            validacion++;
        }
        if(num_serie.equals("")){
            txt_numSerie.setBackground(Color.red);
            validacion++;
        }
        if(observaciones.equals("")){
            jTextPane_observaciones.setText("Sin observaciones.");
        }
        if (validacion == 0) {
            try {
                Connection cn = Conexion.conectar();
                PreparedStatement pst = cn.prepareStatement(
                "update equipos set tipo_equipo=?, marca=?, modelo=?, num_serie=?, observaciones=?, status=?, ultima_modificacion=? "
                        + "where idequipos = '" +IDequipo +"'");
                
                pst.setString(1, tipo_equipo);
                pst.setString(2, marca);
                pst.setString(3, modelo);
                pst.setString(4, num_serie);
                pst.setString(5, observaciones);
                pst.setString(6, status);
                pst.setString(7, user);
                
                pst.executeUpdate();
                cn.close();
                
                Limpiar();
                txt_nombreCliente.setBackground(Color.green);
                txt_fecha.setBackground(Color.green);
                txt_modelo.setBackground(Color.green);
                txt_numSerie.setBackground(Color.green);
                txt_ultmodi.setText(user);
                
                JOptionPane.showMessageDialog(null, "Actualizacion correcta");
                this.dispose();
                
            } catch (SQLException e) {
                System.err.println("Erro en actualizar equipo " + e);
                JOptionPane.showMessageDialog(null, "Error a actualizar equipo, contacte al administrador");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Debes de llenar todos los campos");
        }
    }//GEN-LAST:event_jButton_actualizarActionPerformed

